package Homeworks.L5HW_Java_Arrays_Telpiz_Irina;

public class LengthAndCharAt {
    public static void main(String[] args) {
        String string = "I like Java!";

        System.out.println(string);
        System.out.println("The length of this string is: " + string.length() + " symbols, including SPACES!");
        System.out.println("The 7th symbol (counting starts from ZERO and includes SPACES!) in this string is: " + string.charAt(7));

    }
}
